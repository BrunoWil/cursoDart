// Operadores Lógicos

void main() {
// Operadores lógicos
  bool f = true;
  print('O valor de f= $f');

  bool g = false;
  print('O valor de g= $g');

  bool and = f && g;
  print('O valor de and= f && g = $and');

  bool or = f || g;
  print('O valor de or = f || g = $or');

  bool not = !f;
  print('O valor de not = !f = $not');
}
